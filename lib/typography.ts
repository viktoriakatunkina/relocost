// Русская типографика: прибивает висячие предлоги/союзы к следующему слову
// неразрывным пробелом, связывает числа с единицами, тире — с предыдущим словом.

const NBSP = " ";

const SHORT_WORDS = [
  "а", "в", "и", "к", "о", "с", "у", "я",
  "во", "до", "же", "за", "из", "ко", "ли", "на", "не", "ни", "но", "об", "от", "по", "со", "то", "уж", "бы",
  "без", "для", "над", "обо", "под", "при", "про", "как", "что", "или", "ибо", "его", "ее", "ему",
  "эта", "это", "эти", "тот", "тут", "там", "так", "уже", "еще", "чем",
];

const HANGING_RE = new RegExp(
  `(^|[\\s(«"„(])(${SHORT_WORDS.join("|")})\\s+`,
  "giu",
);

const NUM_UNIT_RE = /(\d)\s+([а-яА-ЯёЁa-zA-Z₽$€£%])/g;

export function typo(input: string | null | undefined): string {
  if (!input) return "";
  let s = input;

  // Висячие предлоги/союзы — два прохода для вложенных кейсов («в и о»)
  s = s.replace(HANGING_RE, (_m, pre, word) => `${pre}${word}${NBSP}`);
  s = s.replace(HANGING_RE, (_m, pre, word) => `${pre}${word}${NBSP}`);

  // Числа + единицы измерения / валюта
  s = s.replace(NUM_UNIT_RE, `$1${NBSP}$2`);

  // Em-dash и en-dash с пробелами — заменим пробел перед тире на неразрывный
  s = s.replace(/ ([—–]) /g, `${NBSP}$1 `);

  return s;
}
